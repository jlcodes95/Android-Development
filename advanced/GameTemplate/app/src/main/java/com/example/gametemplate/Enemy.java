package com.example.gametemplate;

public class Enemy {

    // properties
    int x;
    int y;
    int size;
    int color;
    int speed;

    public Enemy(int initialX, int initialY, int size, int color, int speed) {
        this.x = initialX;
        this.y = initialY;
        this.size = size;
        this.color = color;
        this.speed = speed;
    }





}
